Am 17.August 2017 fand ein <b>Runder Tisch</b> statt im Gemeinschaftszentrums Telli.

<b>Ziel</b> war es, eine sowohl für die AnwohnerInnen des Aaredörfli als auch für die Jugendlichen 
taugliche Lösung des Nutzungskonflikts (Lärmproblematik) rund um das Basketballspielen auf dem 
Schulsportplatz zu finden.

Folgende <b>Parteien</b> nahmen am Runden Tisch teil:
- Vertretung der Stadtpolizei
- Der Hauswart des Tellischulhauses
- Ein Lehrer des Tellischulhauses
- Der Sekretär des Stadtbauamtes
- 3 jugendliche BasketballspielerInnen unterschiedlichen Alters
- Vertretung des GZ Telli
- 4 Vertreter des Aaredörfli
- Jugendbeauftragter der Stadt Aarau (Moderation)

Folgender Kompromiss wurde von den Anwesenden ausgehandelt und vereinbart:

Die Baskettballkörbe werden jeden Tag in der Saison (Frühlingsferien - Mitte November) um 18.00 
von <b>Freiwilligen</b> abgehängt und zusammen mit der Stange für das Auf- bzw. Abhängen, an einem 
abschliessbaren Haken deponiert.  

Der Hauswart hängt die Körbe am Morgen wieder auf, ausser während den Schulferien. Dann übernehmen 
Freiwillige diese Arbeit.

Das GZ Telli ist dafür verantwortlich, dass die Körbe um 18.00 Uhr abgehängt werden.

<b>Wenn Sie sich einen Einsatz am Abend vorstellen können</b>, schauen Sie sich die Datenbank an 
(KNOPF machen, der auf den aktuellen Monat führt). An den farbig hinterlegten Tagen suchen wir 
noch Freiwillige, die die Körbe ab – bzw. aufhängen würden.

<b>Wenn Sie sich für einen Einsatz entschieden haben</b>, melden Sie sich per Mail oder Telefon im 
GZ. Wir geben Ihnen dann das Passwort bekannt und regeln die Schlüsselübergabe

